let marks = 72;  

if (marks >= 75) {
    console.log("Marks:", marks);
    console.log("Grade: A");
} 
else if (marks >= 60) {
    console.log("Marks:", marks);
    console.log("Grade: B");
} 
else if (marks >= 40) {
    console.log("Marks:", marks);
    console.log("Grade: C");
} 
else {
    console.log("Marks:", marks);
    console.log("Result: Fail");
}
